let currentLang = localStorage.getItem('wc_lang') || 'bg';

function applyTranslations(lang) {
  const t = translations[lang];
  document.querySelectorAll('[data-key]').forEach(el => {
    const val = t[el.dataset.key];
    if (val !== undefined) el.innerHTML = val;
  });
  document.documentElement.lang = lang === 'bg' ? 'bg' : 'en';
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('wc_lang', lang);
  const btnBg = document.getElementById('btn-bg');
  const btnEn = document.getElementById('btn-en');
  if (btnBg) btnBg.classList.toggle('active', lang === 'bg');
  if (btnEn) btnEn.classList.toggle('active', lang === 'en');
  applyTranslations(lang);
}

function initLang() {
  const btnBg = document.getElementById('btn-bg');
  const btnEn = document.getElementById('btn-en');
  if (btnBg) btnBg.classList.toggle('active', currentLang === 'bg');
  if (btnEn) btnEn.classList.toggle('active', currentLang === 'en');
  applyTranslations(currentLang);
}
